		/// Defines the event that is raised when the "Cancel" button is pressed.
		/// </summary>
		Cancel,
		/// <summary>
		/// Defines the event that is raised when the "Yes" button is pressed.
		/// </summary>
		Yes,
		/// <summary>
		/// Defines the event that is raised when the "No" button is pressed.
		/// </summary>
		No,
